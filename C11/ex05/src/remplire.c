#include "../includes/main.h"

void	ft_remplire(char **t, void (*f[])(int, int))
{
	t[0] = "+";
	t[1] = "-";
	t[2] = "/";
	t[3] = "*";
	t[4] = "%";
	t[5] = NULL;
	f[0] = add;
	f[1] = sub;
	f[2] = div;
	f[3] = multi;
	f[4] = mod;
}
