#include "../includes/main.h"

void	ft_putnbr(long nb)
{
	if (nb == -9223372036854775807 - 1)
		write(1, "-9223372036854775808", 20);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (0 <= nb && nb <= 9)
		write(1, &(char){nb + '0'}, 1);
}
