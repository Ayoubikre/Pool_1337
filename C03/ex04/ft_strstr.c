#include <unistd.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;
	int	s;

	s = 0;
	while (to_find[s])
		s++;
	if (s == 0)
		return (str);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (str[i + j] == to_find[j] && to_find[j])
			j++;
		if (j == s)
			return (str + i);
		i++;
	}
	return (NULL);
}
