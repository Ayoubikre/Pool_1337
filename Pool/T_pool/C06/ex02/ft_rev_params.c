#include <unistd.h>

int	main(int c, char **t)
{
	int	i;

	if (c < 2)
		return (0);
	i = c - 1;
	while (i > 0)
	{
		while (*t[i])
			write(1, t[i]++, 1);
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
