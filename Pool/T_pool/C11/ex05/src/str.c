#include "../includes/main.h"

void	ft_putstr(char *t)
{
	while (*t)
		write(1, t++, 1);
}

int	ft_strcmp(char *t, char *t2)
{
	int	i;

	i = 0;
	while (t[i] && t[i] == t2[i])
		i++;
	return (t[i] - t2[i]);
}
