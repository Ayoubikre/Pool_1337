#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	
	if (10 <= nb)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (0 <= nb && nb < 10)
	{
		c = nb + '0';
		write(1, &c, 1);
	}
}
