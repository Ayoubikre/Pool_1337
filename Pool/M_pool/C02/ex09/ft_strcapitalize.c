#include <unistd.h>

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (97 <= str[i] && str[i] <= 122)
		{
			if ((i == 0) || (!((48 <= str[i - 1] && str[i - 1] <= 57)
						|| (65 <= str[i - 1] && str[i - 1] <= 90)
						|| (97 <= str[i - 1] && str[i - 1] <= 122))))
				str[i] -= 32;
		}
		else if ((65 <= str[i] && str[i] <= 90))
		{
			if ((48 <= str[i - 1] && str[i - 1] <= 57)
				|| (65 <= str[i - 1] && str[i - 1] <= 90)
				|| (97 <= str[i - 1] && str[i - 1] <= 122))
				str[i] += 32;
		}
		i++;
	}
	return (str);
}
