#include <unistd.h>

void	ft_print_comb2(void)
{
	char	nb;
	int		a;
	int		b;

	a = -1;
	while (++a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			nb = a / 10 + '0';
			write(1, &nb, 1);
			nb = a % 10 + '0';
			write(1, &nb, 1);
			write(1, " ", 1);
			nb = b / 10 + '0';
			write(1, &nb, 1);
			nb = b % 10 + '0';
			write(1, &nb, 1);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
	}
}
