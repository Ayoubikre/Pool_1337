    s = strlen(t);
    for (int i = 0; i < s; i++)
    {
        if (t[i] == 'e')
        {
            for (int j = i + 1; j < s; j++)
            {
                temp = t[j - 1];
                t[j - 1] = t[j];
                t[j] = temp;
            }
            t[s - 1] = '\0';
            i--;
        }
    }
